// increment, decrement bir artirma ve bir azaltma islemi yapar//
// `--` 1 azaltir
// `++` 1 artirir


let ab = 3;
let xy = --ab; // bu satirda ab =2 // azalt sonra ata
console.log(ab, xy)   // bu satirda ab = 2


let abc = 3;
let xyz = abc-- // bu satirda abc = 3 //ilk once ata sonra azalt
console.log(xyz, abc)


